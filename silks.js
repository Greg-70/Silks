function setPattern(pType, pAreaName, pArea) {
	if (pType === 'None')
		return;
	
	var colours = [];
	svgContainer.selectAll("pattern").remove();
	
	if (pAreaName === 'Body') {
		colours = [ $("#bodyColour").val(), $("#bodyColour2").val() ];
		//bodyPatterns.clear()
		pArea.style("fill", colours[0]);
	}
	
	if (pType === 'Block') {
		var pattern = svgContainer.append("pattern")
            .attr("id", "blockPattern")
            .attr("patternUnits", "userSpaceOnUse")
            .attr("width", 40)
			.attr("height", 40)
			.attr("patternTransform", "translate(5,10)");
		pattern.append("rect")
            .attr("height", 20)
            .attr("width", 20)
			.attr("fill", colours[0]);
		pattern.append("rect")
			.attr("x", 20)
            .attr("height", 20)
            .attr("width", 20)
			.attr("fill", colours[1]);
		pattern.append("rect")
			.attr("y", 20)
            .attr("height", 20)
            .attr("width", 20)
			.attr("fill", colours[1]);
		pattern.append("rect")
			.attr("x", 20)
			.attr("y", 20)
            .attr("height", 20)
            .attr("width", 20)
			.attr("fill", colours[0]);
		
		pArea.style("fill", "url(#blockPattern)");
	}
	else if (pType === 'Stripe') {
		bodyGroup.append("rect")
			.attr("id", "stripePattern")
			.attr("x", 108)
			.attr("y", 80)
			.attr("width", 53)
			.attr("height", 194) 
			.attr("mask", "url(#bodyMask)")
			.attr("fill", colours[1]);
	}
}

