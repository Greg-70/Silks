$(function () {
	var $dropdownC = $(".colourSelect");
	$dropdownC.append($("<option />").val('#FFFFFF').text('White'));
	$dropdownC.append($("<option />").val('#000000').text('Black'));
	$dropdownC.append($("<option />").val('#FF0000').text('Red'));
	$dropdownC.append($("<option />").val('#008000').text('Green'));
	$dropdownC.append($("<option />").val('#0000FF').text('Blue'));
	
	var $dropdownP = $(".patternSelect");
	$dropdownP.append($("<option />").val('None').text('None'));
	$dropdownP.append($("<option />").val('Block').text('Block'));
	$dropdownP.append($("<option />").val('Stripe').text('Stripe'));
	
	$(document).on('change', 'select', function() {
		if ($(this).attr('id') === 'bodyColour') {
			bodyPath.style("fill", $(this).val());
			collarPath.style("fill", $(this).val());
			setPattern($("#bodyPattern").val(), 'Body', bodyPath);
			$("#collarColour").val($(this).val());
		}
		else if ($(this).attr('id') === 'bodyColour2') {
			setPattern($("#bodyPattern").val(), 'Body', bodyPath);
		}
		else if ($(this).attr('id') === 'collarColour') {
			collarPath.style("fill", $(this).val());
		}
		else if ($(this).attr('id') === 'sleeveColour') {
			leftSleevePath.style("fill", $(this).val());
			rightSleevePath.style("fill", $(this).val());
			leftCuffPath.style("fill", $(this).val());
			rightCuffPath.style("fill", $(this).val());
			$("#cuffColour").val($(this).val());
		}
		else if ($(this).attr('id') === 'cuffColour') {
			leftCuffPath.style("fill", $(this).val());
			rightCuffPath.style("fill", $(this).val());
		}
		else if ($(this).attr('id') === 'capColour') {
			capPath.style("fill", $(this).val());
			peakPath.style("fill", $(this).val());
			$("#peakColour").val($(this).val());
		}
		else if ($(this).attr('id') === 'peakColour') {
			peakPath.style("fill", $(this).val());
		}
		else if ($(this).attr('id') === 'bodyPattern') {
			setPattern($(this).val(), 'Body', bodyPath);
		}
	});
});

